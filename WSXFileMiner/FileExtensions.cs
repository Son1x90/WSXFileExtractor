        {
            long streamPositionOrigin = stream.Position;
            stream.Position = streamPosition;
            stream.Read(m_SignatureBuffer, 0, m_SignatureBuffer.Length);
            stream.Position = streamPositionOrigin;
            uint matches = 0;
            string matchedExtensions = "";

            //loop through all FileExtension Structs
            for (int i = 0; i < m_fileExtension.Length; i++)
            {
                //loop through all FileExtension Signature bytes
                for (int j = 0; j < m_fileExtension[i].signature.Count; j++)
                {
                    //Temp
                    var test1 = m_fileExtension[i].signature[j];
                    var test2 = m_SignatureBuffer[j];

                    //do the bytes match?
                    if (m_fileExtension[i].signature[j] != m_SignatureBuffer[j])
                    {
                        break;
                    }

                    //if all bytes match
                    if (j == m_fileExtension[i].signature.Count - 1)
                    {
                        matches++;
                        matchedExtensions += "." + m_fileExtension[i].extension;
                        //return m_fileExtension[i].extension;
                    }
                }
            }
            if (matches == 0)
                return "";
            else
                return matchedExtensions;
